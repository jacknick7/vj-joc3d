

public static class GlobalVars {

    static int level, route, radioCh, radioSong;
    static float time;

    public static int Level {
        get { return level; }
        set { level = value; }
    }

    public static int Route {
        get { return route; }
        set { route = value; }
    }

    public static float Time {
        get { return time; }
        set { time = value; }
    }

    public static int RadioCh {
        get { return radioCh; }
        set { radioCh = value; }
    }

    public static int RadioSong {
        get { return radioSong; }
        set { radioSong = value; }
    }
}
